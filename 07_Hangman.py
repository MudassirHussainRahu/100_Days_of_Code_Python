def GameOver(chances, guessed):
    if '_' not in guessed:
        print('Congratulations You Won!!!')

        return True
    elif chances == len(stages):
        print('You Ran out of lives.')
        print(f'Correct word is:{word} ')
        print('You Lose.')
        return True
    else:
        return False


def indices(word, letter):
    indicesl = []
    for i in range(len(word)):
        if word[i] == letter:
            indicesl.append(i)
        else:
            pass
    return indicesl


def guess(word, guessed):
    word = word
    guessed = guessed
    chances = 0
    while not GameOver(chances, guessed):
        print(''.join(guessed))
        letter = input('Guess a letter:').lower()
        if letter in word:
            indixes = indices(word, letter)
            for i in indixes:
                guessed[i] = letter
            print(stages[-chances])
        else:
            chances +=1
            print(stages[-chances])
            print(f'You guessed {letter} that is not in word.')


